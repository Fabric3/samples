package org.fabric3.samples.operations.java;

/**
 * A port name/number pair.
 *
 *
 */
public class Pair {
    private String name;
    private String number;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getNumber() {
        return number;
    }

    public void setNumber(String number) {
        this.number = number;
    }
}
