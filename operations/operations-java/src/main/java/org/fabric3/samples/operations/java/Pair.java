package org.fabric3.samples.operations.java;

/**
 * A port name/number pair.
 *
 * @version $Rev$ $Date$
 */
public class Pair {
    private String name;
    private String port;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPort() {
        return port;
    }

    public void setPort(String port) {
        this.port = port;
    }
}
